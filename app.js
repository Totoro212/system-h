        weeklyCount: 0, // ADDED: Global weekly progress counter (0-7)
        weeklyResetDate: '', // ADDED: Track Monday string to zero the counter
        activeBet: null, // { amount: number, targetDays: number, startDate: string, daysPassed: number }
