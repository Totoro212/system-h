function getDayData(date) {
    if (date === getToday() && data.lastActiveDay !== date) {
        data.lastActiveDay = date;
        // Auto-select 2 random bonus quests for the day
        const shuffled = [...ALL_BONUS_QUESTS].sort(() => 0.5 - Math.random());
        data.bonusQuests = shuffled.slice(0, 2).map(q => ({...q})); 
        saveData();
    }
    if (!data.days[date]) {
        data.days[date] = { main: [], bonus: [], points: 0, done: false };
    }
    return data.days[date];
}

function getRank(points) {
    for (let i = RANKS.length - 1; i >= 0; i--) {
        if (points >= RANKS[i].min) return RANKS[i];
    }
    return RANKS[0];
}

function getRankIndex(points) {
    for (let i = RANKS.length - 1; i >= 0; i--) {
        if (points >= RANKS[i].min) return i;
    }
    return 0;
}

function calcStreak() {
    let streak = 0;
    const today = new Date();
    const todayStr = getToday();
    const todayData = data.days[todayStr];
    const mainCount = data.mainQuests.length;
    
    if (todayData && (todayData.isRestDay || (todayData.main && todayData.main.length === mainCount))) {
        streak = 1;
    }
    
    let checkDate = new Date(today);
    checkDate.setDate(checkDate.getDate() - 1);
    let freezesUsed = 0;
    const MAX_FREEZES = 1; // Защитная заморозка на 1 пропущенный день
    
    while (true) {
        const ds = `${checkDate.getFullYear()}-${String(checkDate.getMonth() + 1).padStart(2, '0')}-${String(checkDate.getDate()).padStart(2, '0')}`;
        const dData = data.days[ds];

        if (!dData) {
            if (freezesUsed < MAX_FREEZES) {
                freezesUsed++;
                checkDate.setDate(checkDate.getDate() - 1);
                continue;
            } else {
                break;
            }
        }

        if (dData.isRestDay) {
            streak++;
            checkDate.setDate(checkDate.getDate() - 1);
            continue;
        }

        const requiredMainLength = dData.hardMode ? 2 : data.mainQuests.length;
        if (dData.main && dData.main.length >= requiredMainLength) {
            streak++;
            checkDate.setDate(checkDate.getDate() - 1);
        } else {
            if (freezesUsed < MAX_FREEZES) {
                freezesUsed++;
                checkDate.setDate(checkDate.getDate() - 1);
            } else {
                break;
            }
        }
    }
    return streak;
}

function calcDaysInSystem() {
    const today = getToday();
    const start = data.startDate || today;
    const [sy, sm, sd] = start.split('-').map(Number);
    const [ty, tm, td] = today.split('-').map(Number);
    const startDate = new Date(sy, sm - 1, sd);
    const todayDate = new Date(ty, tm - 1, td);
    return Math.max(1, Math.round((todayDate - startDate) / 86400000) + 1);
}

function getWeekNumber() {
    const start = new Date(data.startDate || getToday());
    return Math.floor((new Date() - start) / 604800000);
}

