function _weekKey() {
    const n = new Date(), y = n.getFullYear();
    const s = new Date(y, 0, 1);
    const w = Math.ceil(((n - s) / 86400000 + s.getDay() + 1) / 7);
    return `${y}-W${String(w).padStart(2,'0')}`;
}

